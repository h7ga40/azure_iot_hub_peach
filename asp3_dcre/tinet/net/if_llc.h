#ifndef _IF_LLC_H_
#define _IF_LLC_H_

#ifdef SUPPORT_ETHER

/*
 *  長さの定義
 */

#define SNAP_ORG_LEN		3
#define SNAP_TYPE_LEN		2

/*
 *  802.3 ヘッダ
 */

typedef struct t_8023mac_header {
	uint8_t		dhost[ETHER_ADDR_LEN];
	uint8_t		shost[ETHER_ADDR_LEN];
	uint16_t	len;
	} __attribute__((packed, aligned(2)))T_8023MAC_HDR;

typedef struct t_8022llc_header {
	uint8_t		dsap;
	uint8_t		ssap;
	uint8_t		control;
	} __attribute__((packed, aligned(2)))T_8022LLC_HDR;

typedef struct t_8022snap_header {
	uint8_t		original[SNAP_ORG_LEN];
	uint8_t		type[SNAP_TYPE_LEN];
	} __attribute__((packed, aligned(2)))T_8022SNAP_HDR;

#define GET_8023MAC_HDR(nbuf)		((T_8023MAC_HDR*)((nbuf)->buf))
#define GET_8022LLC_HDR(nbuf)		((T_8022LLC_HDR*)((nbuf)->buf + sizeof(T_8023MAC_HDR)))
#define GET_8022SNAP_HDR(nbuf)		((T_8022SNAP_HDR*)((nbuf)->buf + sizeof(T_8023MAC_HDR) + sizeof(T_8022LLC_HDR)))

#endif	/* of #ifdef SUPPORT_ETHER */

#endif	/* of #ifndef _IF_LLC_H_ */
